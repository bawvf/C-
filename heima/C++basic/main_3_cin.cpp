#include <iostream>
#include <string>
using namespace std;

int main() {
    //1.整型
    int a = 0;
    cout<<"给整型赋值:"<<endl;
    cin >> a;
    cout<<a<<endl;
    
//    2.浮点型
    float b = 5.3f;
    cout<<"给float型赋值:"<<endl;
    cin >> b;
    cout<<b<<endl;
    
//    3.字符型
    char c = 'a';
    cout<<"给char型赋值:"<<endl;
    cin >> c;
    cout<<c<<endl;
    
//    4.字符串型
    string d = "Hello";
    cout<<"给stringf型赋值:"<<endl;
    cin >> d;
    cout<<d<<endl;
//    system("pause");
    
    return 0;
}