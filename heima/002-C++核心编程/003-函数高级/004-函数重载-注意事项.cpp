#include <iostream>
using namespace std;

//函数重载的注意事项
//1、 引用作为重载的条件
void func(int &a) {
	cout << "函数(int &a)调用" << endl;
}

void func(const int &a) {
	cout << "函数(const int &a)调用" << endl;
}
//2、函数重载碰到默认参数
void func2(int a, int b = 10) {
	cout << "函数(int a, int b)调用" << endl;
}

void func2(int a) {
	cout << "函数(int a)调用" << endl;
}

int main() {
	int a = 10;
	func(a);

	func(10);

	func2(10);
	

	return 0;
}